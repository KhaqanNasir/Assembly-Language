.STACK 100H
.DATA
 
 MSG DB 'ENTER THE DECIMAL NUMBER $'
 MSG1 DB 0DH,0AH,'BINARY NUMBER IS $'
 
 .CODE 
 
 MOV AX,@DATA
 MOV DS,AX
  LEA DX,MSG
  MOV AH,09H
  INT 21H
  
  MOV AH,01H
  INT 21H  
  
  SUB AL,'0'
  MOV AH,0
  MOV BX,2
  MOV DX,0
  MOV CX,0
 AGAIN: 
  DIV BX
  PUSH DX
  INC CX
  CMP AX,0
  JNE AGAIN 
  
  LEA DX,MSG1
  MOV AH,09H
  INT 21H
DISPLAY:  
  POP DX
  ADD DX,'0'
  MOV AH,02H
  INT 21H
  LOOP DISPLAY  
  
  MOV AH,4CH
